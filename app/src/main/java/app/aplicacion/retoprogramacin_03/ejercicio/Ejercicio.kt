los videojuegos).

Bajo consulta, el sistema debe poder mostrar todos los videojuegos o películas
existentes,


mostrar los que están actualmente alquilados (todas las copias)
y los que están sin alquilar.
Debe permitir consultar si existe un cierto videojuego o película, lo cual se hará
utilizando el Nombre o el Título, según corresponda.
El número de registro lo generará de manera
automática el programa a la hora de insertar una nueva película o videojuego en el sistema.

El alquiler y devolución se realizará utilizando el número de registro. Hay que tener en cuenta
que sólo puede alquilarse cuando exista alguna copia de esa película o videojuego disponible.
